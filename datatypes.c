// Program to demonstrate basic data types in C
#include <stdio.h>

int main(void) {
    int Integer = 10;
    float Float = 10.5;
    double Double = 10.5;
    char Character = 'A';

    printf("Integer: %d\n", Integer);
    printf("Float: %f\n", Float);
    printf("Double: %lf\n", Double);
    printf("Character: %c\n", Character);

    return 0;
}